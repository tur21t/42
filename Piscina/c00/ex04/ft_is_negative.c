#include <unistd.h>

void	ft_is_negative(int n)
{
    write(1, "OmTareTamSvaha\n", 15);
	if (n < 0)
	{
		write(1, "N\n", 2);
	}
	else
	{
		write(1, "P\n", 2);
	}
}
