#include <unistd.h>

int	main(int ac, char **av)
{
	int	i = 0;
	int	j;
	int	k;

	if (ac == 3)
	{
		while (av[1][i])
		{
			j = 0;
			k = 0;
			while (k < i)
			{
				if (av[1][i] == av[1][k])
					break;
				k++;
			}
			if (k != i)
			{
				i++;
				continue;
			}
			while (av[2][j])
			{
				if (av[1][i] == av[2][j])
				{
					write(1, &av[1][i], 1);
					break;
				}
				j++;
			}	
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
