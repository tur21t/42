#include "ft_list.h"

int	ft_list_size(t_list *begin_list)
{
	int	i = 0;

	while(begin_list)
	{
		begin_list = begin_list->next;
		i++;	
	}
	return (i);
}

void	ft_list_foreach(t_list *begin_list, void (*f)(void *))
{
	while (begin_list)
	{
		f(begin_list->data);
		begin_list = begin_list->next;
	}
}
