int	ft_isfloat(char *s)
{
	int	dot;

	dot = 0;
	if (*s == '+' || *s == '-')
		s++;
	if (!*s)
		return (0);
	while (*s)
	{
		if (*s == '.')
		{
			if (dot)
				return (0);
			dot = 1;
		}
		else if (*s < '0' || *s > '9')
			return (0);
		s++;
	}
	return (1);
}
