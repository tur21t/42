#include "ft_printf.h"

void	ft_putstr_pf(char *s, size_t *counter)
{
	size_t	i;

	if (s == NULL)
		s = "(null)";
	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar_pf(s[i], counter);
		i++;
	}
}
