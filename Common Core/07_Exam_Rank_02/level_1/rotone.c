#include <unistd.h>

int	main(int ac, char **av)
{
	int		i;
	char	c;
	if (ac == 2)
	{
		i = 0;
	
	
		while (av[1][i])
		{
			c = av[1][i];
			if (c >= 'a' && c <= 'z')
			{
				if (c == 'z')
					c -= 25;
				else
					c += 1;
			}
			else if (c >= 'A' && c <= 'Z')
			{
				if (c == 'Z')
					c -= 25;
				else
					c += 1;
			}
			write(1, &c, 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
