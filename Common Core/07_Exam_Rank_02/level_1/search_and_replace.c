#include <unistd.h>

int	main(int ac, char **av)
{
	int		i;
	char	c;
	char	src = av[3][0];
	char	dst = av[2][0];
	if (ac == 4 && av[2][1] == '\0' && av[3][1] == '\0')
	{
		i = 0;
	
	
		while (av[1][i])
		{
			c = av[1][i];
			if (c == dst)
			{
				c = src;
			}
			write(1, &c, 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
